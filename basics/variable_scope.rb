# VARIABLE SCOPE

# Local variables are local to the code construct in which they are declared
_localVar = "this is a local variable"

# Use of global variables is strongly discouraged.
$globalVar = "this is a global variable"

# A class variable is a variable that is shared amongst all instances of a class. 
# This means that only one variable value exists for all objects instantiated from this class.
@@classVar = "this is a class variable"

# Instance variables are similar to Class variables except that their values are local to
# specific instances of an object
@instanceVar = "this is an instance variable"